// Adding ints
println(1 + 2) // Operator notation
println((1).+(2))

// Adding ints and longs
println(1 + 2L) // Operator notation
println((1).+(2L))

// Strings
val s = "Hello, world!"
println(s.indexOf('o'))
println(s indexOf 'o') // Operator notation
println(s.indexOf('o', 5))
println(s indexOf ('o', 5)) // Operator notation

